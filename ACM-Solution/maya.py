import sys
p=[1, 20, 360, 7200, 144000, 2880000, 57600000]
maya=["S",".","..","...","....","-",". -",".. -","... -",".... -","- -", ". - -", ".. - -", "... - -", ".... - -","- - -", ". - - -", ".. - - -", "... - - -", ".... - - -"]
n=int(input())
while n!=0:
    i=n-1
    r=0
    while(i>=0):
        x=input()
        for j in range(0,20):
            if(x==maya[j]):break
        if(j!=0):r+=(p[i]*j)
        i-=1
    print(r)
    input()
    n=int(input())
